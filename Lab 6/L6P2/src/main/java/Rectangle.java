public class Rectangle {
    /**
     * Default constructor
     */
    public Rectangle(){}

    /**
     *
     * @param w Width of Rectangle
     * @param h Height of Rectangle
     * @return Area of rectangle (w x h)
     */
    public static int getArea(int w, int h) {
        return w * h;
    }
}
